@Composable
fun BottomNavBar(navController: NavController, navItemSelected: Int, onItemSelected: (Int) -> Unit) {
    NavigationBar {
        BottomNavItem().bottomNavigationItems().forEachIndexed { ind, item ->
            NavigationBarItem(
                selected = ind == navItemSelected,
                onClick = {
                    onItemSelected(ind)
                    navController.navigate(item.route)
                },
                icon = { Icon(item.icon, contentDescription = item.label) },
                label = { Text(item.label) }
            )
        }
    }
}
